class SearchResult {
  final String avatar;
  final String title;
  final int members;
  final String onlineStatus;
  final List<String> tags;
  final String description;

  SearchResult({
    required this.avatar,
    required this.title,
    required this.members,
    required this.onlineStatus,
    required this.tags,
    required this.description,
  });
}
